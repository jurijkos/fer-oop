/** The class TreeNode implements node of binary tree. All nodes in 
 *  binary trees are made as instances of these class.
 * 
 * @author Jurij Kos
 *
 */

public class TreeNode {
	/**This variable is used as a pointer to the left part of the tree.*/
	public TreeNode left;
	/**This variable is used as a pointer to the right part of the tree. */
	public TreeNode right;
	/**It stores value of node.*/
	public String data;
}
